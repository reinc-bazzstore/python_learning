# 数値型
age = 25
height = 5.8

# 文字列型
name = "John"

# リスト型
fruits = ["apple", "banana", "cherry"]

# タプル型（変更不可）
coordinates = (10, 20)

# 辞書型（キーと値のペア）
person = {
    "name": "Alice",
    "age": 30
}

# 変数の値を出力
print(f"Name: {name}, Age: {age}, Height: {height}")
print(f"Fruits: {fruits}")
print(f"Coordinates: {coordinates}")
print(f"Person Info: {person}")