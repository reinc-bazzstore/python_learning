# 算術演算子
x = 10
y = 3
print(x + y)  # 足し算
print(x - y)  # 引き算
print(x * y)  # 掛け算
print(x / y)  # 割り算
print(x % y)  # 余り

# 比較演算子
print(x == y)  # 等しいか
print(x > y)   # より大きいか

# 論理演算子
is_raining = True
is_windy = False
print(is_raining and is_windy)  # 両方ともTrueか
print(is_raining or is_windy)   # どちらかがTrueか
print(not is_raining)           # 否定